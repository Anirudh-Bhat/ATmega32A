void options_window()
{
	sse();
	PORTB|=(1<<DC);
	setxy(0,0);
	for(int t=0;t<504;t++)
	{
		transmit(opening[t]);
	}
	ssd();
}
void display_car(int move)
{
	sse();
	PORTB|=(1<<DC);
	setxy(0,move);
	for(int i=0;i<12;i++)
	transmit(carh[i]);
	ssd();
}
void move_car(int move)
{
	sse();
	PORTB|=(1<<DC);
	setxy(0,move);
	for(int i=0;i<12;i++)
	transmit(0x00);
	ssd();
}



void display_food(int x,int y)
{
	sse();
	PORTB|=(1<<DC);
	for(int i=8;i>=-1;i--)
	{
		if(x>=0)
		{
		setxy(x,y);
		transmit(fd[i]);
		}
		x--;
	}
}
