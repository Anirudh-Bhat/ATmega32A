void spi_init()
{
	DDRB=0xff;
	SPCR=(1<<MSTR)|(1<<SPR0)|(1<<SPE);
}
void sse()
{
	DDRB&=~(1<<ss);
}
void ssd()
{
	DDRB|=(1<<ss);
}
void reset_display()
{
	PORTB&=~(1<<reset);
	_delay_ms(1);
	PORTB|=(1<<reset);
}
void transmit(unsigned char data)
{
	SPDR=data;
	while(!(SPSR&(1<<SPIF)));
}
void transmitc(unsigned char data)
{
	sse();
	PORTB&=~(1<<DC);
	transmit(data);
	PORTB|=(1<<DC);
	ssd();
}
void display_init()
{
	transmitc(0x21);//h=1 addition mode

	transmitc(0xC0);//vop 5v;

	transmitc(0x07);// set temperature co efficient 3

	transmitc(0x13);//bias voltage

	transmitc(0x20);// basic mode(pd=h=v=0)

	transmitc(0x0C);// normal mode(normal display mode)
}
