void display_data(char *data)
{
	sse();
	PORTB|=(1<<DC);
	int k=strlen(data);
	for(int i=0;i<k;i++)
	{
		for(int j=0;j<5;j++)
		{
			transmit(ASCII[data[i]-0x20][j]);
		}transmit(0x00);
	}
	ssd();
}
